#include "ft_printf.h"

int	ft_putdigit(int a)
{
	int	i;

	i = 0;
	if (a == -2147483648)
	{
		i += 2;
		write(1, "-2", 2);
		a = 147483648;
	}
	if (a < 0)
	{
		write(1, "-", 1);
		i++;
		a = a * -1;
	}
	if (a > 9)
		i += ft_putdigit(a / 10);
	i ++;
	a = a % 10;
	a += 48;
	write(1, &a, 1);
	return (i);
}
